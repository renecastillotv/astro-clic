/**
 * Obtiene un texto UI en el idioma especificado
 * @param {string} key - Clave del texto (ej: 'PRICE_ON_CONSULTATION')
 * @param {string} language - Idioma ('es', 'en', 'fr')
 * @param {string} fallback - Texto de respaldo si no se encuentra la clave
 * @returns {string} Texto traducido
 */ export function getUIText(key, language = 'es', fallback = '') {
  const textEntry = UI_TEXTS[key];
  if (!textEntry) {
    console.warn(`UI Text key '${key}' not found`);
    return fallback || key;
  }
  return textEntry[language] || textEntry.es || fallback || key;
}
/**
 * Obtiene múltiples textos UI de una vez
 * @param {Array<string>} keys - Array de claves de texto
 * @param {string} language - Idioma ('es', 'en', 'fr')
 * @returns {Object} Objeto con las claves y sus textos traducidos
 */ export function getUITexts(keys, language = 'es') {
  const result = {};
  keys.forEach((key)=>{
    result[key] = getUIText(key, language);
  });
  return result;
}
/**
 * Obtiene todos los textos UI para un idioma específico
 * @param {string} language - Idioma ('es', 'en', 'fr')
 * @returns {Object} Objeto con todas las claves y sus textos traducidos
 */ export function getAllUITexts(language = 'es') {
  const result = {};
  Object.keys(UI_TEXTS).forEach((key)=>{
    result[key] = getUIText(key, language);
  });
  return result;
}
/**
 * Formatea precio con moneda y sufijo
 * @param {number} price - Precio numérico
 * @param {string} currency - Moneda ('USD', 'DOP')
 * @param {string} type - Tipo de precio ('sale', 'rental', 'temp_rental', 'furnished_rental')
 * @param {string} language - Idioma
 * @returns {string} Precio formateado
 */ export function formatPrice(price, currency, type = 'sale', language = 'es') {
  if (!price || !currency) {
    return getUIText('PRICE_ON_CONSULTATION', language);
  }
  const currencySymbol = currency === 'USD' ? 'USD$' : 'RD$';
  const formattedAmount = `${currencySymbol}${price.toLocaleString()}`;
  if (type === 'rental' || type === 'furnished_rental') {
    return `${formattedAmount}${getUIText('MONTHLY_RENT', language)}`;
  } else if (type === 'temp_rental') {
    return `${formattedAmount}${getUIText('NIGHTLY_RATE', language)}`;
  }
  return formattedAmount;
}
/**
 * Construye título de carrusel contextual
 * @param {Object} group - Grupo de tags del carrusel
 * @param {Array} userTags - Tags del usuario para contexto
 * @param {string} language - Idioma
 * @returns {string} Título completo del carrusel
 */ export function buildCarouselTitle(group, userTags, language = 'es') {
  const getTagDisplayName = (tag)=>{
    if (!tag) return null;
    if (language === 'en' && tag.display_name_en) return tag.display_name_en;
    if (language === 'fr' && tag.display_name_fr) return tag.display_name_fr;
    return tag.display_name || tag.name;
  };
  const operationTag = userTags?.find((tag)=>tag.category === 'operacion');
  const categoryTag = userTags?.find((tag)=>tag.category === 'categoria');
  const cityTag = userTags?.find((tag)=>tag.category === 'ciudad');
  const sectorTag = userTags?.find((tag)=>tag.category === 'sector');
  let titleParts = [
    getUIText('DISCOVER_OUR', language)
  ];
  // Tipo de propiedad (pluralizado)
  if (categoryTag) {
    let categoryDisplay = getTagDisplayName(categoryTag);
    if (!categoryDisplay.toLowerCase().endsWith('s')) {
      categoryDisplay += 's';
    }
    titleParts.push(categoryDisplay.toLowerCase());
  } else {
    titleParts.push(getUIText('PROPERTIES', language).toLowerCase());
  }
  // Ubicación
  if (sectorTag) {
    titleParts.push(getUIText('LOCATION_IN', language), getTagDisplayName(sectorTag));
  } else if (cityTag) {
    titleParts.push(getUIText('LOCATION_IN', language), getTagDisplayName(cityTag));
  }
  // Característica especial del carrusel
  const specialFeature = group.seo_title || group.name;
  if (specialFeature) {
    titleParts.push(specialFeature.toLowerCase());
  }
  return titleParts.join(' ');
}
